public class Loop {
    public static void main(String[] args) {
        System.out.println("Using loop: ");
        int i = 100;
        while(i<= 200){
            System.out.println(i);
            i++;
        }
        System.out.println("Finished!");

        int a = 1;
        do{
            System.out.println(a);
            a++;
        } while(a <= 45);
        System.out.println("Finished Do-While loop");

    }
}
