public class ForLoop {
    public static void main(String[] args) {
        System.out.println("Using for loop: ");
        for(int i = 1; i<=10 ; i++){
            System.out.println(i);
        }

        System.out.println("Odd numbers: ");
        for(int n = 0; n<5; n++){
            System.out.println(2*n + 1);
        }

        System.out.println("Reverse numbers: ");
        for(int i = 10; i!= 0;i--){
            System.out.println(i);
        }


    }
}
