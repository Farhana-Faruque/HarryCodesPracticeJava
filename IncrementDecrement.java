public class IncrementDecrement {
    public static void main(String[] args) {
        int i = 45;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);

        int y = 7;
        int x = ++y * 8;
        System.out.println(x);
    }
}
